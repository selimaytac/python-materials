# Addition
shopping_total = 150.50
tax_rate = 0.08
total_amount = shopping_total + (shopping_total * tax_rate)
print(total_amount) # Output: 162.54

# Subtraction
account_balance = 1000.0
withdrawal_amount = 250.0
new_balance = account_balance - withdrawal_amount
print(new_balance) # Output: 750.0

# Multiplication
flour_per_serving = 50.0
number_of_servings = 10
total_flour_needed = flour_per_serving * number_of_servings
print(total_flour_needed) # Output: 500.0

# Division (float)
total_income = 100000.0
number_of_days = 30
average_income_per_day = total_income / number_of_days
print(average_income_per_day) # Output: 3333.3333333333335

# Division (integer)
total_items = 100
items_per_box = 12
number_of_boxes = total_items // items_per_box
print(number_of_boxes) # Output: 8

# Modulo (remainder)
minutes_per_hour = 60
laps_per_minute = 5
total_laps = 127
remaining_laps = total_laps % (minutes_per_hour * laps_per_minute)
print(remaining_laps) # Output: 7

# Exponentiation
length = 10
width = 5
area = length ** 2 + width ** 2
print(area) # Output: 125