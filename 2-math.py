# Addition
a = 5 + 3
print(a)    # Output: 8

# Subtraction
b = 7 - 2
print(b)    # Output: 5

# Multiplication
c = 4 * 6
print(c)    # Output: 24

# Division (float)
d = 10 / 3
print(d)    # Output: 3.3333333333333335

# Division (integer)
e = 10 // 3
print(e)    # Output: 3

# Modulo (remainder)
f = 10 % 3
print(f)    # Output: 1

# Exponentiation
f = 2 ** 3
print(f)    # Output: 8


